    Alpine.data('langToggle', () => ({
        visible: true,
        init() {
            const hero = document.querySelector('[data-hero]');
            if (!hero) return;
            new IntersectionObserver(([entry]) => {
                this.visible = entry.isIntersecting;
            }, { threshold: 0 }).observe(hero);
        }
    }));

